function [t,r] = GetPols(x,y)

%==========================================================================
% Take precaution when coordinate is on one of the crack faces, i.e.:
% x = -ev & y = 0 ==> t = pi or -pi
%==========================================================================

r = sqrt(x.^2+y.^2);
t = zeros(size(r));

i = find(r>0);
t(i) = acos(x(i)./r(i));

i = i(y(i) < 0);
t(i) = -t(i);

%==========================================================================

end


